export function useDroneHubSidebarProps(args: any): DroneSidebarProps {
  const {
    dronesError,
    groupMoveError,
    dronesLoading,
    sidebarDronesFilteredByRepo,
    sidebarDrones,
    sidebarOptimisticDroneIdSet,
    selectedDroneSet,
    selectedIsResponding,
    deletingDrones,
    renamingDrones,
    settingBaseImages,
    movingDroneGroups,
    sidebarGroups,
    collapsedGroups,
    deletingGroups,
    renamingGroups,
    dragOverGroup,
    sidebarHasUngroupedGroup,
    draggingDroneNames,
    dragOverUngrouped,
    repos,
    reposLoading,
    reposError,
    drones,
    droneCountByRepoPath,
    uiDroneName,
    openDraftChatComposer,
    openCreateModal,
    selectDroneCard,
    openCloneModal,
    renameDrone,
    setDroneBaseImage,
    deleteDrone,
    openDroneErrorModal,
    onUngroupedDragOver,
    onUngroupedDragLeave,
    onUngroupedDrop,
    onGroupDragOver,
    onGroupDragLeave,
    onGroupDrop,
    createGroupAndMove,
    setCollapsedGroups,
    renameGroup,
    openGroupMultiChat,
    deleteGroup,
    onDroneDragStart,
    onDroneDragEnd,
    setReposModalOpen,
  } = args;

  return React.useMemo(
    () => ({
      dronesError,
      groupMoveError,
      dronesLoading,
      sidebarDronesFilteredByRepo,
      sidebarDrones,
      sidebarOptimisticDroneIdSet,
      selectedDroneSet,
      selectedIsResponding,
      deletingDrones,
      renamingDrones,
      settingBaseImages,
      movingDroneGroups,
      sidebarGroups,
      collapsedGroups,
      deletingGroups,
      renamingGroups,
      dragOverGroup,
      sidebarHasUngroupedGroup,
      draggingDroneNames,
      dragOverUngrouped,
      repos,
      reposLoading,
      reposError,
      dronesCount: drones.length,
      droneCountByRepoPath,
      uiDroneName,
      onOpenDraftChatComposer: openDraftChatComposer,
      onOpenCreateModal: openCreateModal,
      onSelectDroneCard: selectDroneCard,
      onOpenCloneModal: openCloneModal,
      onRenameDrone: renameDrone,
      onSetDroneBaseImage: (droneId) => {
        void setDroneBaseImage(droneId);
      },
      onDeleteDrone: deleteDrone,
      onOpenDroneErrorModal: openDroneErrorModal,
      onUngroupedDragOver,
      onUngroupedDragLeave,
      onUngroupedDrop,
      onGroupDragOver,
      onGroupDragLeave,
      onGroupDrop,
      onCreateGroupAndMove: createGroupAndMove,
      onToggleGroupCollapsed: (group) =>
        setCollapsedGroups((prev: Record<string, boolean>) => ({
          ...prev,
          [group]: !prev[group],
        })),
      onRenameGroup: (group) => {
        void renameGroup(group);
      },
      onOpenGroupMultiChat: (group) => {
        openGroupMultiChat(group);
      },
      onDeleteGroup: (group, count) => {
        void deleteGroup(group, count);
      },
      onDroneDragStart,
      onDroneDragEnd,
      onOpenReposModal: () => setReposModalOpen(true),
    }),
    [args],
  );
}

